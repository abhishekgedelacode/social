public class Numbers {
    public static void main(String args[]) {
        int dec = 10, bin = 0b10, oct = 010, hex = 0x10;
        System.out.println("dec: " + dec);
        System.out.println("bin: " + bin);
        System.out.println("oct: " + oct);
        System.out.println("hex: " + hex);
    }
}